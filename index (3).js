// -----------------------------------------------------------
// Завдання 1. Парне чи непарне число
// -----------------------------------------------------------
let number = prompt("Введіть число:");
number = Number(number);

let result = (number % 2 === 0) ? "парне" : "непарне";
alert(`Число ${number} є ${result}.`);

// -----------------------------------------------------------
// Завдання 2. Сума чисел від 1 до 100
// -----------------------------------------------------------
let sum = 0;

for (let i = 1; i <= 100; i++) {
  sum += i;
  console.log(`Ітерація ${i}: поточна сума = ${sum}`);
}

console.log(`Кінцева сума чисел від 1 до 100: ${sum}`);

