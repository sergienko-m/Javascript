// -----------------------------------------------------------
// Завдання 1. Масив квадратів чисел
// -----------------------------------------------------------
function getSquares(arr) {
  return arr.map(num => num * num);
}

const numbers1 = [1, 2, 3, 4];
console.log("=== Завдання 1. Квадрати чисел ===");
console.log("Вхідний масив:", numbers1);
console.log("Результат:", getSquares(numbers1)); // [1, 4, 9, 16]

// -----------------------------------------------------------
// Завдання 2. Найбільше число з масиву
// -----------------------------------------------------------
function getMax(arr) {
  return Math.max(...arr);
}

const numbers2 = [3, 5, 1, 8, 4];
console.log("\n=== Завдання 2. Найбільше число ===");
console.log("Вхідний масив:", numbers2);
console.log("Результат:", getMax(numbers2)); // 8

// -----------------------------------------------------------
// Завдання 3. Унікальні рядки з масиву
// -----------------------------------------------------------
function getUnique(arr) {
  return [...new Set(arr)];
}

const strings = ["a", "b", "a", "c", "b"];
console.log("\n=== Завдання 3. Унікальні рядки ===");
console.log("Вхідний масив:", strings);
console.log("Результат:", getUnique(strings)); // ["a", "b", "c"]
